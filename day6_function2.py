#2.返回多个值
def 计算长方形的周长和面积(长,宽):
    面积 = 长 * 宽
    周长 = 2 * (长 + 宽)
    return 面积,周长

print("\n2.返回多个值:")
面积,周长 = 计算长方形的周长和面积(5,3)
print(f"长3宽5的长方形:面积 = {面积},周长 = {周长}")

#3.文档字符串（函数说明）
def 计算圆的面积(半径):
    """
    计算圆的面积 

    参数：
    半径 —— 圆的半径 （数字）

    返回
    圆的面积
    """
    import math
    return math.pi * 半径 ** 2

print("\n3. 文档字符串：")
print(计算圆的面积.__doc__)  # 查看函数说明
print(f"半径为3的圆面积：{计算圆的面积(3):.2f}")


# 4. 函数调用函数
def 计算平方(x):
    return x * x

def 计算立方(x):
    return 计算平方(x) * x  # 调用另一个函数

print("\n4. 函数调用函数：")
print(f"3的平方:{计算平方(3)}")
print(f"3的立方:{计算立方(3)}")

# 5. 可变参数
def 计算平均数(*数字):
    """计算任意多个数字的平均数"""
    if len(数字) == 0:
        return 0
    return sum(数字) / len(数字)

print("\n5. 可变参数：")
print(f"1,2,3的平均数:{计算平均数(1, 2, 3):.2f}")
print(f"10,20,30,40的平均数:{计算平均数(10, 20, 30, 40):.2f}")
print(f"5,5,5,5,5的平均数:{计算平均数(5, 5, 5, 5, 5):.2f}")
