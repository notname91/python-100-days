# 第2天：字符串操作学习
print("🔤 字符串操作大全")
print("=" * 40)

# 1. 基本字符串
字符串1 = "你好，"
字符串2 = "世界！"

# 2. 字符串拼接
完整问候 = 字符串1 + 字符串2
print(f"1. 字符串拼接: {完整问候}")

# 3. 字符串重复
欢呼 = "万岁！" * 3
print(f"2. 字符串重复: {欢呼}")

# 4. 字符串长度
我的名字 = "张三"
print(f"3. 字符串长度: '张三' 的长度是 {len(我的名字)}")

# 5. 字符串索引（获取单个字符）
print(f"4. 字符串索引: '张三' 的第一个字符是 '{我的名字[0]}'")
print(f"               '张三' 的第二个字符是 '{我的名字[1]}'")

# 6. 字符串切片（获取一部分）
长字符串 = "Python编程很有趣"
print(f"5. 字符串切片: '{长字符串[0:6]}'")  # 获取前6个字符
print(f"               '{长字符串[6:]}'")   # 获取第6个字符之后

# 7. 字符串方法
句子 = "  hello, WORLD!  "

print(f"6. 字符串方法示例:")
print(f"   原始: '{句子}'")
print(f"   去掉空格: '{句子.strip()}'")
print(f"   全部大写: '{句子.upper()}'")
print(f"   全部小写: '{句子.lower()}'")
print(f"   首字母大写: '{句子.strip().capitalize()}'")
print(f"   单词首字母大写: '{句子.strip().title()}'")

# 8. 字符串替换
文本 = "我喜欢吃苹果"
新文本 = 文本.replace("苹果", "香蕉")
print(f"7. 字符串替换: '{文本}' → '{新文本}'")

# 9. 字符串查找
print(f"8. 字符串查找: '喜欢' 在 '{文本}' 中的位置: {文本.find('喜欢')}")
print(f"              '西瓜' 在 '{文本}' 中的位置: {文本.find('西瓜')} (没找到返回-1)")

# 10. 字符串格式化（三种方法）
名字 = "李四"
年龄 = 30

方法1 = "我的名字是：" + 名字 + "，年龄是：" + str(年龄)
方法2 = "我的名字是：{}，年龄是：{}".format(名字, 年龄)
方法3 = f"我的名字是：{名字}，年龄是：{年龄}"

print(f"\n9. 字符串格式化:")
print(f"   方法1（拼接）: {方法1}")
print(f"   方法2（format）: {方法2}")
print(f"   方法3（f-string）: {方法3}")
