#第二天项目：个人信息卡片生成器
print("个人信息卡片生成器")
print("=" * 40)

print("请填写以下信息：")

#收集用户信息
姓名 = input("你的姓名: ")
年龄 = input("你的年龄: ")
职业 = input("你的职业: ")
城市 = input("你所在的城市: ")
爱好 = input("你的爱好: ")
技能 = input("你的技能 (用逗号分隔): ")
邮箱 = input("你的邮箱: ")

#处理技能（字符串分割）
技能列表 = 技能.split(',')

#生成卡片
卡片 = f"""
{'=' * 50}
{" " * 50}* 个人卡片 *
{"=" * 50}

  姓名: {姓名:>15}
  年龄: {年龄:>15}
  职业: {职业:>15}
  城市: {城市:>15}
  邮箱: {邮箱:>15}
  
  爱好: {爱好}
  
  技能清单:
"""

# 添加技能项目符号
for i, 技能项 in enumerate(技能列表, 1):
    卡片 += f"    {i}. {技能项.strip()}\n"

卡片 += f"""
{'=' * 50}
{' ' * 15}卡片生成完成！
{'=' * 50}
"""

print(卡片)

# 询问是否保存到文件
保存 = input("\n是否保存到文件？(y/n): ").lower()

if 保存 == 'y':
    with open(f"{姓名}_个人名片.txt", "w", encoding="utf-8") as 文件:
        文件.write(卡片)
    print(f"✅ 名片已保存到 '{姓名}_个人名片.txt'")
else:
    print("❌ 未保存文件")