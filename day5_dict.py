#第五天：学习字典
print("第五天：学习字典（Dicitionary）")
print("=" * 40)

#1.创建字典——就像电话本
电话本 = {
    "妈妈": "13800138000",
    "爸爸": "13900139000", 
    "小明": "13700137000",
    "小红": "13600136000"
}
print("1.我的电话本：")
print(电话本)

#2.访问字典中的值
print("\n2.访问字典：")
print("   妈妈的电话：",电话本["妈妈"])
print("   小明的电话：",电话本.get("小明"))

#3.添加新的联系人
print("\n3.添加新的联系人：")
电话本["老师"] = "13500135000"
print("    添加老师后：",电话本)

# 4. 修改字典
print("\n4. 修改电话：")
电话本["小红"] = "13600666000"  # 小红换号码了
print("   修改小红电话后：", 电话本)

# 5. 删除联系人
print("\n5. 删除联系人：")
del 电话本["小明"]  # 小明转学了
print("   删除小明后：", 电话本)

# 6. 检查是否存在
print("\n6. 检查联系人：")
if "妈妈" in 电话本:
    print("   ✅ 妈妈在电话本里")
else:
    print("   ❌ 妈妈不在电话本里")

if "小明" in 电话本:
    print("   ✅ 小明在电话本里")
else:
    print("   ❌ 小明不在电话本里")

# 7. 遍历字典
print("\n7. 所有联系人：")
for 姓名, 电话 in 电话本.items():
    print(f"   {姓名}: {电话}")

# 8. 字典信息
print("\n8. 字典信息：")
print("   共有", len(电话本), "个联系人")
print("   所有姓名：", list(电话本.keys()))
print("   所有电话：", list(电话本.values()))
