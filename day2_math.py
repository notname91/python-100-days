#第二天：数学运算学习
print("Python数学运算")
print("=" * 40)

#用户输入两个数字
print("请输入两个数字进行计算：")
数字1 = float(input('第一个数字：'))
数字2 = float(input('第二个数字：'))

#基本计算
print("\n基本运算结果：")
print(f"  {数字1} + {数字2} = {数字1 + 数字2}")
print(f"  {数字1} - {数字2} = {数字1 - 数字2}")
print(f"  {数字1} × {数字2} = {数字1 * 数字2}")
print(f"  {数字1} ÷ {数字2} = {数字1 / 数字2}")

#进阶计算
print("\n进阶运算结果：")
print(f"  整数除法: {数字1} // {数字2} = {数字1 // 数字2}")
print(f"  取余数: {数字1} % {数字2} = {数字1 % 数字2}")
print(f"  乘方: {数字1} 的 {数字2} 次方 = {数字1 ** 数字2}")

#数学函数
import math # 导入数学模块

print('\n数学函数结果：')
print(f"  平方根：{数字1} = {math.sqrt(数字1)}")
print(f"  绝对值：|{数字1} - {数字2}| = {abs(数字1 - 数字2)}")
print(f"  四舍五入：{数字1 / 数字2} ~ {round(数字1 / 数字2, 2)}")

#练习：计算圆的面积
print("\n 计算圆的面积")
半径 = float(input("请输入圆的半径："))
面积 = math.pi * (半径 ** 2)
周长 = 2 * math.pi * 半径

print(f"   半径 = {半径}")
print(f"   面积 = {面积:.2f}")
print(f'   周长 = {周长:.2f}')





